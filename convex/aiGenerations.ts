import { v } from "convex/values";
import { mutation, query } from "./_generated/server";
import { getAuthenticatedUser } from "./lib/auth";

// Get AI generations for a specific field
export const getGenerations = query({
  args: {
    resumeId: v.id("resumes"),
    fieldType: v.string(),
    fieldId: v.optional(v.string()),
  },
  handler: async (ctx, args) => {
    const user = await getAuthenticatedUser(ctx);
    const resume = await ctx.db.get(args.resumeId);
    if (!resume || resume.userId !== user._id) throw new Error("Not authorized");

    const generations = await ctx.db
      .query("aiGenerations")
      .withIndex("by_resume_field", (q) =>
        q
          .eq("resumeId", args.resumeId)
          .eq("fieldType", args.fieldType)
          .eq("fieldId", args.fieldId)
      )
      .first();

    return generations;
  },
});

// Check if auto-generation has happened for a field
