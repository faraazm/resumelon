// Save a new AI generation
export const saveGeneration = mutation({
  args: {
    resumeId: v.id("resumes"),
    fieldType: v.string(),
    fieldId: v.optional(v.string()),
    originalContent: v.string(),
    generatedContent: v.string(),
    tone: v.string(),
    prompt: v.optional(v.string()),
    isAutoGenerated: v.optional(v.boolean()),
  },
  handler: async (ctx, args) => {
    const user = await getAuthenticatedUser(ctx);
    const resume = await ctx.db.get(args.resumeId);
    if (!resume || resume.userId !== user._id) throw new Error("Not authorized");

    const now = Date.now();

    // Check if a record already exists for this field
    const existing = await ctx.db
      .query("aiGenerations")
      .withIndex("by_resume_field", (q) =>
        q
          .eq("resumeId", args.resumeId)
          .eq("fieldType", args.fieldType)
          .eq("fieldId", args.fieldId)
      )
      .first();

    const newGeneration = {
      content: args.generatedContent,
      tone: args.tone,
      prompt: args.prompt,
      createdAt: now,
    };

    if (existing) {
      // Append to existing generations array, clear selectedIndex since new content was generated
      await ctx.db.patch(existing._id, {
        generations: [...existing.generations, newGeneration],
        originalContent: args.originalContent, // Update original content
        hasAutoGenerated: existing.hasAutoGenerated || args.isAutoGenerated || false,
        selectedIndex: undefined,
        updatedAt: now,
      });
      return existing._id;
    } else {
      // Create new record
      const id = await ctx.db.insert("aiGenerations", {
        resumeId: args.resumeId,
        fieldType: args.fieldType,
        fieldId: args.fieldId,
        originalContent: args.originalContent,
        generations: [newGeneration],
        hasAutoGenerated: args.isAutoGenerated || false,
        createdAt: now,
        updatedAt: now,
      });
      return id;
    }
  },
});

